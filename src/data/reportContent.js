// ============ 跨维度模式分析 ============
export function getPatternAnalysis(dimScores) {
  const patterns = [];
  const sorted = Object.entries(dimScores).sort((a, b) => b[1] - a[1]);
  const [top, second] = sorted;
  const bottom = sorted[sorted.length - 1];

  // Pattern 1: Emotional + Aesthetic both high
  if (dimScores.emotional >= 65 && dimScores.aesthetic >= 65) {
    patterns.push({
      title: '🎨 创造型敏感者',
      body: '你的情绪和审美双重高敏感，这是典型的"创造型敏感者"配置。你能深刻地感受到情绪，又有能力将这种感受转化为美的表达。许多作家、音乐家、画家都具有这种模式。你的内在世界异常丰富，但也要小心——你容易被自己的情绪和外界的美同时淹没。保持创作的习惯，这是你最好的情绪出口。',
    });
  }

  // Pattern 2: Sensory + Emotional both high
  if (dimScores.sensory >= 65 && dimScores.emotional >= 65) {
    patterns.push({
      title: '⚡ 高反应型敏感者',
      body: '你的感官和情绪系统都非常灵敏，这意味着你对外界刺激的反应格外强烈。一把刺眼的灯光、一句伤人的话——这些对别人来说可能只是小困扰，对你来说却是真实的身体和情绪冲击。你需要比常人更主动地管理自己的环境。好消息是：你也同样强烈地感受到生活中的美好。',
    });
  }

  // Pattern 3: Sensory + Social both high
  if (dimScores.sensory >= 65 && dimScores.social >= 65) {
    patterns.push({
      title: '🛡️ 环境依赖型敏感者',
      body: '你同时在感官和社交两个维度高度敏感。这意味你不仅对物理环境敏感，也对他人的情绪敏感。在嘈杂的社交场合中（如拥挤的餐厅、派对），你承受双重压力——物理的嘈杂和社交的情绪负荷。你需要优先考虑环境的质量：安静、私密、人数少的聚会可能更适合你。',
    });
  }

  // Pattern 4: Social highest, sensory low
  if (top[0] === 'social' && dimScores.sensory < 50) {
    patterns.push({
      title: '🤝 人际导向型敏感者',
      body: '你的敏感主要体现在人际方面而非感官方面。你可能在物理环境中很自在，但对人的情绪波动特别敏感。你喜欢深度的一对一交流胜过喧闹的群体活动。这种配置让你成为一个好的倾听者和沟通者——你的敏感正好用在人与人的连接上。',
    });
  }

  // Pattern 5: Aesthetic highest, emotional low
  if (top[0] === 'aesthetic' && dimScores.emotional < 50) {
    patterns.push({
      title: '🖼️ 鉴赏型敏感者',
      body: '你的审美系统高度发达，但情绪系统相对稳定。这是一个很有趣的组合——你能深刻地欣赏美，但不会被美过度扰动你的情绪状态。你像一个冷静的艺术鉴赏家，能同时保持理性和感性。这种组合很适合创意行业中的策划、设计、评论等角色。',
    });
  }

  // Pattern 6: All moderate
  if (sorted.every(([, score]) => score >= 40 && score <= 65)) {
    patterns.push({
      title: '⚖️ 均衡型敏感者',
      body: '你的四个维度都相对均衡，没有特别突出的敏感方面，也没有明显的短板。这让你在各种场景下都能保持相对平稳的状态。你可以有意识地选择发展某一个维度——比如刻意培养审美能力，或者训练自己的共情深度。你的灵活性是你的优势。',
    });
  }

  return patterns;
}

